class Solution{
    public:
    bool wifiRange(int N, string S, int X){
        // code here
          int i = 0, wifi = 0;
        
        while(i < N && wifi >= -X)  S[i++]-'0' ? wifi = X : wifi-- ;
            
        return i==N && wifi>=0;
    }
};

